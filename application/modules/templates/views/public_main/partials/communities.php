                                                    <p><b><br>PARAMUS<br> </b>Paramus was not always the land of enormous malls and shopping centers that has made it one of the top retail zip codes in the U.S. When it was incorporated as a borough in 1922 it was a rural area of celery farms and dirt roads.</p>

                                                    <p>The native Lenni Lenape Indians gave Paramus its name. It comes from the Lenape word “peramsepuss” sometimes translated as “land of the wild turkeys” or “fertile fields where turkeys are found.” A large metal statue of a wild turkey can be found today at the Paramus Park Mall.</p>

                                                    <p>Following the opening of the George Washington Bridge in 1931 and the development boom that followed World War II, Paramus was transformed into a shopping mecca. N.J.’s first shopping centers, the Garden State Plaza and the Bergen Mall (known today as the Bergen Town Center) opened in 1957 and 1958. The borough now contains four major shopping malls and three corporate parks which lie astride Routes 4, 17 and the Garden State Parkway.</p>

                                                    <p>Paramus is home to Bergen Community College, the Bergen Museum of Art and Science, the New Jersey Children’s Museum and four golf courses: Arcola Country Club, Paramus Golf and Country Club, Ridgewood Country Club and Orchard Hills County Golf Course.<b>&nbsp;</b>

