                                                    <p><b><br>BERGENFIELD</b>

                                                    </p>

                                                    <p>Bergenfield is a borough in eastern/central Bergen County and was incorporated in 1894 from portions of the now-gone Palisades Township and Englewood Township. The town takes its name from the 19th century train station that was originally referred to as “Bergen Fields” and later as “Bergenfield”.</p>

                                                    <p>In 2010, New Jersey Monthly magazine ranked Bergenfield as its 211st best place to live in New Jersey. In it’s previous 2008 list, Bergenfield was ranked number 231. In a 2010 Businessweek article, the borough of Bergenfield was ranked as the #2 best place in the state of New Jersey to raise children. It was praised for a community that “pulls together around a true Main Street with such events as the annual holiday display contest”. The article also mentioned the town pool and the numerous town parks.</p>

                                                    <p>Although Bergenfield no longer has train access, the town has public bus service to New York City and is located just a few miles from route 4 and 16 miles from New York City.</p>

