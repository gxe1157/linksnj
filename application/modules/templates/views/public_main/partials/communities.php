                                                    <p>&nbsp;<b>TEANECK&nbsp;</b>

                                                    </p>

                                                    <p>One of the most culturally diverse communities’ in New Jersey is Teaneck. Teaneck is between the Hackensack River and the Palisades of the Hudson River. It is home to over 40,000 residents. There are numerous parks, business and cultural environments. Possibly named for a Dutch family, the Teneycks. Another tradition says the name may derive from an Indian term for "the woods".&nbsp;</p>

                                                    <p>The Township of Teaneck was established on February 19, 1895 and was comprised of parts of Englewood, Hackensack, Ridgefield Park, Bergen Fields and Bogota. The town's population was 811. During these early days of the 20th century, the town's affairs focused on construction of streets and street lamps (originally gaslights), trolley lines (along DeGraw Avenue), telephones and speeding traffic. Social activities centered on church events and harness racing at the popular racetrack on Cedar Lane, near River Road. Several small inns offered fine dining and dancing to overnight guests.</p>

                                                    <p>Express bus service to New York City via the George Washington Bridge and the Lincoln Tunnel is available. Today, Teaneck continues to thrive by respecting its residents' many and diverse voices while working towards a blending of interest so all are at home and welcome here. Teaneck's ethnically diverse community as well as its array of shops and restaurants creates a welcoming environment for international students.</p>

