                                                    <p><b>FAIR LAWN</b>

                                                    </p>

                                                    <p>An historic account of Fair Lawn would be incomplete without the acknowledgment of the Lenni-Lenapi ("original people"), native tribes of northern New Jersey. Their trails, campsites, rock shelters and hunting grounds became the roads and towns we use and reside in today. The first Dutch settlers traveled up, what we now know as, the Hackensack and Passaic Rivers, to establish fur trading posts with the Hackinghaesaky Indians, one of the tribes of the Lenni-Lenapi. The great chief of the tribes was Oratam. As settlements expanded, the Lenni-Lenapi were forced further west to unsettled land. They left behind place names of Indian origin. Few of us realize how many indigenous names still remain, for example: Passaic (either "where the river goes over the falls" or "valley"), Paramus ("fine stream" or "place of wild turkeys"), Wagaraw ("crooked place" or "river bend"). Typically, River Road, one of the oldest roads in the eastern part of our country, was once an Indian trail, leading to the "Great Rock" tribal council site in Glen Rock.</p>

                                                    <p>Fair Lawn's pinnacle of growth was during the 1940s and 1950s. Expansive areas of farm lands were developed for single-family homes and several large garden apartment complexes. The population exploded from 9,000 in 1940 to an estimated peak of about 37,000 in 1968. Fair Lawn Industrial Park (on Route 208) was developed during the 1950s with several additions in the following decade. Among the Industrial Park's corporate residents are internationally known firms such as Kodak, Nabisco and Lea &amp; Perrins.</p>

                                                    <p>By 1970, the remaining large tracts of land had been urbanized. The last farm in Fair Lawn was a 20 acre tract in the Industrial Park at Fair Lawn Avenue. In 1998, this tract was developed into apartments.</p>

                                                    <p>What began as an agricultural community has grown into a suburban town providing homes, schools, parks and shops for residents and jobs for thousands of workers in businesses, offices and industries.</p>

