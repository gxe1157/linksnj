                                                    <p><b><br>NEW MILFORD</b>

                                                    </p>

                                                    <p>New Milford, a borough which spans 2.308 square miles, was officially formed from the remaining land that made up Palisades Township in March of 1922 and is known as the birthplace of Bergen County. The Historic Preservation Commission’s mission is to preserve the integrity of New Milford’s historic heritage in many ways. The Commission aims to fulfill this duty through researching the people, events, and locations that are of historic value and archiving, thus preserving, buildings, land, and historic items. Through public education programs and commendations for borough residents and organizations who have offered assistance with this task, the mission of the Commission, to spread the knowledge of the borough’s history, can thrive.</p>

                                                    <p>With a population of around 16,000 and an area of just over 2 square miles, New Milford's small-town atmosphere is one of its main appeals. It also offers a wide range of relatively affordable single-family houses within commuting distance from Manhattan.</p>

                                                    <p>Students don't have to travel far to the borough's two elementary schools, the community is served by a volunteer fire department and its shopping strips are largely devoid of chain stores and malls.</p>

