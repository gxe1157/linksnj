                                                    <p><b><br> HOBOKEN</b>

                                                    </p>

                                                    <p>Hoboken was first settled as part of the Pavonia, New Netherland colony in the 17th century. During the early 19th century, the city was developed first as a resort and later as a residential neighborhood. It became a township in 1849 and was incorporated as a city in 1855. Its waterfront was an integral part of the Port of New York and New Jersey and home to major industries for most of the 20th century.</p>

                                                    <p>Hoboken is part of the New Jersey Gold Coast. The character of the city has changed from a blue collar town to one of upscale shops and condominiums. The city is part of the New York metropolitan area and contains Hoboken Terminal, a major transportation hub for the region.</p>

                                                    <p>The initial presence of artists and young people changed the perception of the city such that others perceived it as an interesting, safe, exciting, and desirable place to live. The process continued as many suburbanites, transplanted Americans, internationals, and immigrants (mostly focused on opportunities in NY/NJ region and proximity to Manhattan, NY) began to make Hoboken their home, and the "real-estate boom" of the era encouraged many to seek investment opportunities.</p>

