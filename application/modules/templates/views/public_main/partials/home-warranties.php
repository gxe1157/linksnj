                                                    <p>A home warranty is an affordable way to cover the costs of unexpected mechanical failure of a major system or appliance. A home warranty is specifically designed to cover the kinds of repairs that home insurance does not: appliances, plumbing and electrical, air conditioning and furnaces, and pool equipment.</p>

                                                    <p>The average annual cost of a home warranty policy is between $250 and $400. Most home warranty companies offer comparable coverage within the same price range. The premium is payable at closing and customarily protects you for one full year. Repairs are typically handled through the home warranty company with a minimal deductible. Often times the cost of the first year premium is offered as an incentive by sellers to solicit the sale of the property.</p>

                                                    <p>The age and condition of the home should be a consideration when choosing to purchase a home warranty. A fifteen year-old home with original equipment, versus a two year old home will likely have different financial risks. Your REALTOR® can help you decide if a home warranty policy is right for you based on your individual circumstances.</p>

                                                    <h2>Why Should I Consider a Home Warranty?</h2>

                                                    <p>Homeownership is expensive enough all on its own, without adding the cost of repairs and replacements. When moving into a home where appliances and systems have been previously used, there is always the chance that the general wear and tear, or the way in which they were previously used and maintained, could cause breakdown and/or complete failure. These repairs/replacements can be astronomically costly and often times occur unexpectedly. A home warranty will protect you financially from most of the frequently occurring breakdowns of home system components and appliances.</p>

                                                    <p>Discuss your unique needs and concerns with your home warranty representative. If you do not have a trusted home warranty representative, your REALTOR® can refer you to one.</p>

